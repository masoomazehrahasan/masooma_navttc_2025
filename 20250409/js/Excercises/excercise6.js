// Exercise 1: Day of the Week
// Ask the user to enter a number from 1 to 7.
// Use a switch statement to display the corresponding day of the week (e.g., 1 = Sunday, 2 = Monday, etc.).

let DofWek = prompt("Enter a number from 1 to 7 ");
switch (DofWek) {
  case 1:
    "It's Sunday";
    break;
  case 2:
    "It's Monday";
    break;
  case 3:
    "It's Tuesday";
    break;
  case 4:
    "It's Wednesday";
    break;
  case 5:
    "It's Thursday";
    break;
  case 6:
    "It's Friday";
    break;
  case 1:
    "It's Saturday";
    break;
  default:
    "Enter Number Between 1 - 7";
    break;
}
