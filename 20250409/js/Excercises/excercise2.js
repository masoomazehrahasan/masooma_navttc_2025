// Exercise 2: Even or Odd
// Ask the user to enter a number.
// Use if-else to check whether the number is even or odd.

let dgt = 65;
if (dgt % 2 == 0) {
  document.body.innerHTML = "The number " + dgt + " is even";
} else {
  document.body.innerHTML = "The number " + dgt + " is odd";
}
